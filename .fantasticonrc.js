// Sort a codepoints map by key (case-insensitive) so generated assets come
// out A->Z. fantasticon (via glob v9+) no longer sorts discovered SVGs, so
// the CSS/JSON it generates follow whatever order this map is in - sorting it
// here makes that order alphabetical regardless of how entries were appended
// below. Values stay pinned to their id, so codepoints never shift.
// (build:index sorts the .ts/.html/index.ts to match this same order.)
const sortByKey = (obj) =>
  Object.fromEntries(
    Object.entries(obj).sort(([a], [b]) => a.localeCompare(b, undefined, { sensitivity: "base" }))
  );

